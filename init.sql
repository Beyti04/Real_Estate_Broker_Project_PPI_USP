CREATE DATABASE IF NOT EXISTS real_estate_db CHARACTER
SET
    utf8mb4 COLLATE utf8mb4_unicode_ci;

USE real_estate_db;

CREATE TABLE
    IF NOT EXISTS estate_category (
        id INT AUTO_INCREMENT PRIMARY KEY,
        category_name VARCHAR(255) NOT NULL
    );

CREATE TABLE
    IF NOT EXISTS estate_types (
        id INT AUTO_INCREMENT PRIMARY KEY,
        type_name VARCHAR(255) NOT NULL,
        category_id INT NOT NULL,
        FOREIGN KEY (category_id) REFERENCES estate_category (id)
    );

CREATE TABLE
    IF NOT EXISTS regions (
        id INT AUTO_INCREMENT PRIMARY KEY,
        region_name VARCHAR(255) NOT NULL
    );

CREATE TABLE
    IF NOT EXISTS locations (
        id INT AUTO_INCREMENT PRIMARY KEY,
        region_id INT NOT NULL,
        location_name VARCHAR(255) NOT NULL,
        FOREIGN KEY (region_id) REFERENCES regions (id)
    );

CREATE TABLE
    IF NOT EXISTS neighborhoods (
        id INT AUTO_INCREMENT PRIMARY KEY,
        location_id INT NOT NULL,
        neighborhood_name VARCHAR(255) NOT NULL,
        FOREIGN KEY (location_id) REFERENCES locations (id)
    );

CREATE TABLE
    IF NOT EXISTS user_types (
        id INT AUTO_INCREMENT PRIMARY KEY,
        type_name VARCHAR(255) NOT NULL
    );

CREATE TABLE
    IF NOT EXISTS users (
        id INT AUTO_INCREMENT PRIMARY KEY,
        username VARCHAR(255) NOT NULL,
        email VARCHAR(255) NOT NULL UNIQUE,
        password VARCHAR(255) NOT NULL,
        user_type_id INT NOT NULL,
        FOREIGN KEY (user_type_id) REFERENCES user_types (id)
    );

CREATE TABLE
    IF NOT EXISTS estates (
        id INT AUTO_INCREMENT PRIMARY KEY,
        location_id INT NOT NULL,
        estate_type_id INT,
        exposure_type VARCHAR(255) NOT NULL,
        rooms INT NOT NULL,
        description TEXT,
        listing_type VARCHAR(255) NOT NULL,
        price DECIMAL(10, 2) NOT NULL,
        owner_id INT NOT NULL,
        FOREIGN KEY (estate_type_id) REFERENCES estate_types (id),
        FOREIGN KEY (location_id) REFERENCES locations (id),
        FOREIGN KEY (owner_id) REFERENCES users (id)
    );

CREATE TABLE
    IF NOT EXISTS audit_logs (
        id INT AUTO_INCREMENT PRIMARY KEY,
        user_id INT NOT NULL,
        table_name VARCHAR(255) NOT NULL,
        action VARCHAR(255) NOT NULL
    );

